export function t(lang: string, key: string, params?: Record<string, string>): string {
  let str = LABELS[lang]?.[key] ?? LABELS["en"][key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

export function useLanguage() {
  const [lang, setLang] = useState("en");
  
  useEffect(() => {
    // Initial load
    const stored = localStorage.getItem("kw_language");
    if (stored) setLang(stored);

    // Optional: listen for storage changes across tabs or custom events
    const handleStorage = () => {
      const current = localStorage.getItem("kw_language");
      if (current) setLang(current);
    };
    window.addEventListener("storage", handleStorage);
    window.addEventListener("kw_lang_changed", handleStorage);
    
    return () => {
      window.removeEventListener("storage", handleStorage);
      window.removeEventListener("kw_lang_changed", handleStorage);
    };
  }, []);
  
  return lang;
}
